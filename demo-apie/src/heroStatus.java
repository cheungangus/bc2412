public class heroStatus {
    private int hp;
    private int mp;
    private int level;

    public heroStatus(int hp, int mp, int level){
        this.hp = hp;
        this.hp = mp;
        this.level =level;

    }

    public int getHp(){
        return this.hp;
    }
    public int getMp(){
        return this.mp;
    }
    public int level(){
        return this.level;
    }
    
}
